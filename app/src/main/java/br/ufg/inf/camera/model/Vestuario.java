package br.ufg.inf.camera.model;

public class Vestuario {
    private String boneChapeu;
    private String camisaBlusa;
    private String shortCalca;
    private String calcado;
    private String data;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getBoneChapeu() {
        return boneChapeu;
    }

    public void setBoneChapeu(String boneChapeu) {
        this.boneChapeu = boneChapeu;
    }

    public String getCamisaBlusa() {
        return camisaBlusa;
    }

    public void setCamisaBlusa(String camisaBlusa) {
        this.camisaBlusa = camisaBlusa;
    }

    public String getShortCalca() {
        return shortCalca;
    }

    public void setShortCalca(String shortCalca) {
        this.shortCalca = shortCalca;
    }

    public String getCalcado() {
        return calcado;
    }

    public void setCalcado(String calcado) {
        this.calcado = calcado;
    }
}
